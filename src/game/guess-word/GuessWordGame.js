const answers = CLASSIC_RIDDLES.map(([, answer]) => answer);

function createOptions(answer, index) {
  const options = [answer];
  [17, 43, 71].forEach((offset) => options.push(answers[(index + offset) % answers.length]));
  return options
    .map((value, optionIndex) => ({ value, rank: (index * 13 + optionIndex * 19) % 29 }))
    .sort((first, second) => first.rank - second.rank)
    .map(({ value }) => value);
}

export const WORD_PUZZLES = CLASSIC_RIDDLES.map(([clue, answer], index) => ({
  clue,
  answer,
  options: createOptions(answer, index),
  enClue: `Chinese character riddle: ${clue}`,
  enAnswer: answer,
}));

export class GuessWordGame {
  constructor(random = Math.random) {
    this.random = random;
    this.order = [...WORD_PUZZLES].sort(() => this.random() - 0.5).slice(0, 5);
    this.index = 0;
    this.score = 0;
    this.correct = 0;
  }

  get puzzle() { return this.order[this.index] ?? null; }

  answer(value) {
    if (!this.puzzle) return null;
    const correct = value === this.puzzle.answer;
    if (correct) { this.score += 200; this.correct += 1; }
    this.index += 1;
    return { correct, finished: this.index >= this.order.length };
  }
}
